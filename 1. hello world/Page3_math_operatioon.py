# mathematical operation

n1 = 10
n2 = 20

# addition
addition = n1 + n2
print(f"Addition = {addition}")

# multiplication
multiplication = n1 * n2
print(f"Multiplication = {multiplication}")

#substraction
substration = n1 - n2
print(f"Substration = {substration}")

#true division -> float decimal
division = n1/n2
print(f"Division_float = {division}")

#floor division -> int (whole)
division = n1//n2
print(f"Division_floor = {division}")

# 10 to the power of 3
power = 10**3
print(f"Power = {power}")

# remainder
mod = 11%2
print(f"Remainder = {mod}")

# string * operator
print("_*_" * 20)

# Concatination -> string + operator
print("Shree_" + "Ganeshay_" + "Namah")